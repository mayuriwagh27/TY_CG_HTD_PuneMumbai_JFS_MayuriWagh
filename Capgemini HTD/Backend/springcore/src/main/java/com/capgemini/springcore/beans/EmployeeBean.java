package com.capgemini.springcore.beans;

public class EmployeeBean {

	private int empId;
	private String empName;
	
	//Getters and Setters
	public int getEmpId() {
		return empId;
	}
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	
}//end of class
